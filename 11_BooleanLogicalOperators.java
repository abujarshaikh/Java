class BooleanLogicalOperators{
public static void main(String arg[])
{
boolean x1=true;
boolean x2=false;
System.out.println("x1="+x1+" x2="+x2);
System.out.println("x1 & x2: " +(x1&x2));
System.out.println("x1 | x2: " +(x1|x2));
System.out.println(" !x1: " +(!x1));
System.out.println(" !x2: " +(!x2));
System.out.println(" x1 ^ x2: " +(x1^x2));
}
}