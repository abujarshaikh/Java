class ArithmeticOperation{
public static void main(String args[])
{
int a=100;
int b=50;
int c=40;
int d=-100;

System.out.println("Arithmetic Operation On Two Interger Value");
System.out.println("a="+a+" b="+b+" c="+c+" d="+d);
System.out.println("Addition (a+b)::"+(a+b));
System.out.println("Substraction (a-b)::"+(a-b));
System.out.println("Multiplication (a*b)::" +(a*b));
System.out.println("Division (a/b)::"+(a/b));
System.out.println("Remainder Ater Division (a%c)::"+(a%c));
System.out.println("Remainder After Division (d%c)::"+(d%c));
System.out.println();
double e=100.00;
double f=50.25;
double g=40.25;
double h=-100.0;

System.out.println("Arithmetic Operation On Two Double Value");
System.out.println("e="+e+" f="+f+" g="+g+" h="+h);
System.out.println("Addition (e+f)::"+(e+f));
System.out.println("Substraction (e-f)::"+(e-f));
System.out.println("Multiplication (e*f)::" +(e*f));
System.out.println("Division (e/f)::"+(e/f));
System.out.println("Remainder Ater Division (e%g)::"+(e%g));
System.out.println("Remainder After Division (h%g)::"+(h%g));
System.out.println();
float i=100.00f;
float j=50.25f;

System.out.println("Arithmetic Operation On Two Float Value");
System.out.println("i="+i+" j="+j);
System.out.println("Remainder After Division (i%j)::"+(i%j));
}}